package com.Day1;

public class LetterOrder {
  public static void main(String[] args) {
        char ch1 = 'z';
        char ch2 = 'o';
        
        if (ch1 < ch2) {
            System.out.println(ch1 + "," + ch2);
        } else {
            System.out.println(ch2 + "," + ch1);
        }
    }
}
